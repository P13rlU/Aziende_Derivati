-- Creazione Tabelle
create table a_DIPENDENTE
(
    ID_DIPENDENTE varchar2(25)
        primary key,
    NOME          VARCHAR2(50) not null,
    COGNOME       VARCHAR2(50) not null,
    STIPENDIO     NUMBER(10, 2) default 0,
    SETTORE       VARCHAR2(50) not null
        constraint check_settore check (settore in ('produzione', 'vendita', 'direzione', 'Controllo Qualità')),
    CATEGORIA     VARCHAR2(50)
        constraint check_categoria check (categoria in ('latte', 'carne', 'grano'))
);

create table a_prodotto
(
    nome                varchar2(100)
        primary key,
    categoria           varchar2(50)  not null,
    costo_di_produzione number(10, 2) not null,
    constraint check_costo_di_produzione
        check (costo_di_produzione > 0),
    constraint check_categoria_a_prodotto check (categoria in ('latte', 'carne', 'grano'))
);

create table a_cliente
(
    ID_CLIENTE number generated by default as identity
        primary key,
    nome       varchar2(50) not null,
    cognome    varchar2(50) not null,
    citta      varchar2(50) not null
);

alter table a_cliente modify id_cliente generated always as identity (start with 36);
commit;

ALTER TABLE A_CLIENTE ADD CODICE_FISCALE VARCHAR2(16);

create table a_fattura
(
    ID_FATTURA   varchar2(25)
        primary key,
    ID_VENDITORE varchar2(25)  not null
        constraint fattura_DIPENDENTE_ID_DIPENDENTE_fk
            references a_DIPENDENTE,
    ID_CLIENTE   number        not null
        constraint fattura_CLIENTE_ID_CLIENTE_fk
            references a_cliente,
    data_vendita date          not null,
    totale       number(10, 2) not null,
    constraint check_totale
        check (totale > 0)
);

create table a_dettaglio
(
    ID_FATTURA varchar2(25)
        constraint fk_fattura
            references a_FATTURA,
    prodotto   varchar2(100)
        constraint fk_prodotto
            references a_PRODOTTO,
    quantita   number        not null,
    costo      number(10, 2) not null,
    constraint pk_dettaglio
        primary key (ID_FATTURA, prodotto)
);

create table A_Gestione
(
    Id_dipendente     varchar2(25) NOT NULL,
    Data_assegnazione Date         NOT NULL,
    Settore           varchar2(20) NOT NULL,
    Categoria         varchar2(20),
    constraint a_gestione_pk primary key (id_dipendente, data_assegnazione),
    constraint a_gestione_id_dipendente_fk foreign key (id_dipendente) references a_dipendente (id_dipendente)
        on delete set null,
--     constraint a_gestione_settore_chk check (settore in ('produzione', 'vendita', 'direzione', 'Controllo Qualità')),
    constraint a_gestione_categoria_chk check (categoria in ('latte', 'carne', 'grano'))
);

